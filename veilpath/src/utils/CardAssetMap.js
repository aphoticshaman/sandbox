/**
 * Get asset source from static map
 * @param {string} assetPath - Path from manifest (e.g., "assets/art/card_art/common/fool.png")
 * @returns {any} Asset source object for Image/Video components
 */
export function getAssetSource(assetPath) {
  const asset = CARD_ASSET_MAP[assetPath];
  if (!asset) {
    console.warn(`Asset not found in static map: ${assetPath}`);
    return null;
  }
  return asset;
}

/**
 * Check if an asset path exists in the map
 * @param {string} assetPath - Path to check
 * @returns {boolean}
 */
export function hasAsset(assetPath) {
  return assetPath in CARD_ASSET_MAP;
}

